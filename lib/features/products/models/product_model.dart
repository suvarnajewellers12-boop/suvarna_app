class Product {
  final String id;
  final String name;
  final String weight;
  final String image;
  final String category;
  final String metal;
  final String purity;
  final String making;
  final bool hallmark;
  final String description;

  Product({
    required this.id,
    required this.name,
    required this.weight,
    required this.image,
    required this.category,
    required this.metal,
    required this.purity,
    required this.making,
    required this.hallmark,
    required this.description,
  });
}